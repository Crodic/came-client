const ItemBar = () => {
    const [data, setData] = useState([]);

    useEffect(() => {
        fetchCategory();
    }, []);

    const handleClick = (id) => {
        console.log(id);
    };

    const fetchCategory = async () => {
        try {
            let res = await getAllCategory();
            if (res && res.status === 200) {
                const { categories } = res.data;
                if (categories) {
                    const custom = categories.map((item) => {
                        return {
                            ...item,
                            icon: IconBar[item.title],
                        };
                    });
                    setData(custom);
                }
            }
        } catch (error) {}
    };

    return (
        <ul>
            {data &&
                data.length > 0 &&
                data.map((category) => {
                    return (
                        <li
                            key={category._id}
                            className="px-[20px] py-[10px]"
                            onClick={() => handleClick(category._id)}
                        >
                            <Link
                                to={`/products?category=${category._id}`}
                                className="flex"
                            >
                                {React.createElement(`${category.icon.type}`, {
                                    ...category.icon.props,
                                })}
                                <span className="ml-5 font-normal hover:text-red-500 transition-all duration-100 cursor-pointer">
                                    {category.title}
                                </span>
                            </Link>
                        </li>
                    );
                })}
        </ul>
    );
};

export default ItemBar;
